package standard.classes.MathClass;

public class Math_Class {
    /*
        Class Math merupakan class utilities yang berisikan
        banyak sekali static method untuk operasi numerik,
        seperti trigonometric, logarithm, akar pangkat, dan
        lain-lain

        Lebih lengkap buka dokumentasi
     */
    public static void main(String[] args) {

        var min = Math.min(4, 2);
        System.out.println(min);

        var max = Math.max(6,4);
        System.out.println(max);

        var hasi_cos = Math.cos(30);
        System.out.println(hasi_cos);

        System.out.println(Math.PI);

    }
}
